myString = "Christmas is coming"
# Prints the first character (C):
print(myString[0])
# Prints the sixth character (t):
print(myString[5])

# Prints 0, 1 and 2 (Chr):
print(myString[0:3])
# Prints ist:
print(myString[3:6])

# Prints the last character (g):
print(myString[-1])
# Prints from the third last character to the end of the string (ing):
print(myString[-3:])
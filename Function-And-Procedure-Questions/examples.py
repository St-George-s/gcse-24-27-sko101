# Example

# Procedure
def printName(name):
    print(name)

# Function
def printNameFunc(name):
    return name

# Call the procedure
printName("Liyana")

# Call the function and print returned value
print(printNameFunc("Vidgis"))

# Call the function and store returned value in a variable
returnedName = printNameFunc("Alina")
print(returnedName)
